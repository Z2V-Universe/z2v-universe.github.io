import os
import re

# Настройки путей
OUTPUT_DIR = 'articles'

def slugify(text):
    """
    Превращает любой текст в безопасный URL-friendly формат (латиница, без пробелов).
    """
    # Простая таблица транслитерации (Кириллица -> Латиница)
    translit_map = {
        'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
        'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
        'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
        'ф': 'f', 'х': 'kh', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'shch',
        'ъ': '', 'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya'
    }
    
    text = text.lower()
    # Применяем транслитерацию
    res = ""
    for char in text:
        res += translit_map.get(char, char)
    
    # Убираем всё, что не буквы, не цифры и не дефис
    res = re_clean(res)
    return res

def re_clean(text):
    # Оставляем только латинские буквы, цифры и дефис
    return re.sub(r'[^a-z0-9\-]', '-', text).replace('--', '-').strip('-')

def create_structured_article(raw_data):
    try:
        if '[METADATA_BLOCK]' not in raw_data or '[RAW_CONTENT]' not in raw_data:
            print("❌ Ошибка: Не найдены блоки [METADDATA_BLOCK] или [RAW_CONTENT]")
            return

        parts = raw_data.split('[METADATA_BLOCK]')
        sub_parts = parts[1].split('[RAW_CONTENT]')
        
        metadata_part = sub_parts[0]
        content_part = sub_parts[1]

        metadata = {}
        for line in metadata_part.strip().split('\n'):
            if ':' in line:
                key, val = line.split(':', 1)
                metadata[key.strip()] = val.strip().replace('"', '')

        md_content = "---\n"
        for key, val in metadata.items():
            md_content += f"{key}: {val}\n"
        md_content += "---\n\n"
        md_content += content_part.strip()

        if not os.path.exists(OUTPUT_DIR):
            os.makedirs(OUTPUT_DIR)

        # --- ГЛАВНОЕ ИЗМЕНЕНИЕ ТУТ ---
        name_val = metadata.get('name', 'unknown')
        # Превращаем "I-ый_йикоулд" -> "i-yj_yikould"
        safe_name = slugify(name_val)
        
        file_name = f"{safe_name}.md"
        output_path = os.path.join(OUTPUT_DIR, file_name)

        with open(output_path, 'w', encoding='utf-8') as f:
            f.write(md_content)
        
        print(f"✅ Файл создан: {output_path} (из '{name_val}')")

    except Exception as e:
        print(f"❌ Ошибка: {e}")

