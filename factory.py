def create_page(raw_data):
    try:
        # 1. Разделяем текст на блоки
        if '[METADATA_BLOCK]' not in raw_data or '[RAW_CONTENT]' not in raw_data:
            print("❌ Ошибка: Не найдены блоки [METADATA_BLOCK] или [RAW_CONTENT]")
            return

        parts = raw_data.split('[METADATA_BLOCK]')
        metadata_part = parts[1].split('[RAW_CONTENT]')[0]
        content_part = parts[1].split('[RAW_CONTENT]')[1]

        # 2. Парсим метаданные в словарь
        metadata = {}
        for line in metadata_part.strip().split('\n'):
            if ':' in line:
                key, val = line.split(':', 1)
                metadata[key.strip()] = val.strip()

        # 3. Подготавливаем данные для шаблона
        title = metadata.get('name', 'Без названия')
        
        # Формируем контекст для Jinja2
        context = {
            'title': title,
            'content': content_part.strip().replace('\n', '<br>'),
            'location': metadata.get('location', 'Неизвестно'),
            'languages': metadata.get('languages', 'Неизвестно'),
            'capital': metadata.get('capital', 'Неизвестно'),
            'currency': metadata.get('currency', 'Неизвестно'),
            'status': metadata.get('status', 'Неизвестно')
        }

        # 4. Загружаем шаблон и рендерим
        if not os.path.exists(TEMPLATE_FILE):
            print(f"❌ Ошибка: Файл шаблона {TEMPLATE_FILE} не найден!")
            return

        with open(TEMPLATE_FILE, 'r', encoding='utf-8') as f:
            template_html = f.read()
        
        template = Template(template_html)
        rendered_html = template.render(context)

        # 5. Сохраняем результат
        if not os.path.exists(OUTPUT_DIR):
            os.makedirs(OUTPUT_DIR)
        
        file_name = f"{title.lower().replace(' ', '_')}.html"
        output_path = os.path.join(OUTPUT_DIR, file_name)
        
        with open(output_path, 'w', encoding='utf-8') as f:
            f.write(rendered_html)
        
        print(f"✅ Страница '{title}' успешно создана: {output_path}")

    except Exception as e:
        print(f"❌ Произошла ошибка при создании страницы: {e}")

if __name__ == "__main__":
    create_page(INPUT_TEXT)